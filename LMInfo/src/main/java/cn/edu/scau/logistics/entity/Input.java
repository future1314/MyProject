package cn.edu.scau.logistics.entity;

public class Input {
	private int inputId;
	private int ordertableDetailId;
	private String inputCategory;
	private String inputName;
	private int inputNum;
	private int inputTrueNum;
	private String inputTime;
	private String isInput;
	private String inputOther;
	public int getInputId() {
		return inputId;
	}
	public void setInputId(int inputId) {
		this.inputId = inputId;
	}
	public int getOrdertableDetailId() {
		return ordertableDetailId;
	}
	public void setOrdertableDetailId(int ordertableDetailId) {
		this.ordertableDetailId = ordertableDetailId;
	}
	public String getInputCategory() {
		return inputCategory;
	}
	public void setInputCategory(String inputCategory) {
		this.inputCategory = inputCategory;
	}
	public String getInputName() {
		return inputName;
	}
	public void setInputName(String inputName) {
		this.inputName = inputName;
	}
	public int getInputNum() {
		return inputNum;
	}
	public void setInputNum(int inputNum) {
		this.inputNum = inputNum;
	}
	public int getInputTrueNum() {
		return inputTrueNum;
	}
	public void setInputTrueNum(int inputTrueNum) {
		this.inputTrueNum = inputTrueNum;
	}
	public String getInputTime() {
		return inputTime;
	}
	public void setInputTime(String inputTime) {
		this.inputTime = inputTime;
	}
	public String getIsInput() {
		return isInput;
	}
	public void setIsInput(String isInput) {
		this.isInput = isInput;
	}
	public String getInputOther() {
		return inputOther;
	}
	public void setInputOther(String inputOther) {
		this.inputOther = inputOther;
	}
	
}
